
public class Nodo {
  private int i;
  private int j;
  private Double valor;
  
  public Nodo(int i, int j, Double valor) {
    this.i = i;
    this.j = j;
    this.valor = valor;
  }

  public int getI() {
    return i;
  }

  public void setI(int i) {
    this.i = i;
  }

  public int getJ() {
    return j;
  }

  public void setJ(int j) {
    this.j = j;
  }

  public Double getValor() {
    return valor;
  }

  public void setValor(Double valor) {
    this.valor = valor;
  }
  
}
